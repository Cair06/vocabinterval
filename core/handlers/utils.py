async def update_card_creation_message(bot_message_id, instruction_message_id, state, message, **kwargs):
    card_info = await state.get_data()
    card_info.update(kwargs)

    await message.bot.edit_message_text(
        chat_id=message.chat.id,
        message_id=bot_message_id,
        text=(
                '–°–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—É—é –∫–∞—Ä—Ç–æ—á–∫—É!üìù\n'
                '<i>–ï—Å–ª–∏ —É –≤–∞—Å –∏–º–µ—é—Ç—Å—è –Ω–µ—Å–∫–æ–ª—å–∫–æ —Å–ª–æ–≤/–ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–π –≤ –æ–¥–Ω–æ–º –∏–∑ —Ä–∞–∑–¥–µ–ª–æ–≤ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ –∏—Ö –∑–∞–ø—è—Ç—ã–º–∏</i>\n\n'
                f'–°–ª–æ–≤–æ: {card_info.get("card_foreign_word", "üö´")}' + (
                    " ‚úÖ" if card_info.get("card_foreign_word") is not None else "") + '\n'
                                                                                      f'–ü–µ—Ä–µ–≤–æ–¥: {card_info.get("card_translation", "üö´")}' + (
                    " ‚úÖ" if card_info.get("card_translation") is not None else "") + '\n'
                                                                                     f'–¢—Ä–∞–Ω—Å–∫—Ä–∏–ø—Ü–∏—è: {card_info.get("card_transcription", "üö´")}' + (
                    " ‚úÖ" if card_info.get("card_transcription") is not None else "") + '\n'
                                                                                       f'–ö–æ–Ω—Ç–µ–∫—Å—Ç: {card_info.get("card_example_usage", "üö´")}' + (
                    " ‚úÖ" if card_info.get("card_example_usage") is not None else "")
        )
    )
    # –†–µ–¥–∞–∫—Ç–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–µ–π –Ω–∞ —Å–ª–µ–¥—É—é—â–∏–π —à–∞–≥
    next_step = card_info.get("next_step")
    if next_step:
        await message.bot.edit_message_text(
            chat_id=message.chat.id,
            message_id=instruction_message_id,
            text=next_step
        )
    await state.update_data(**card_info)


